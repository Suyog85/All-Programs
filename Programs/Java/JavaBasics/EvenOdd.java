import java.util.*;
public class EvenOdd {
    public static void main(String Args[]){
        Scanner sc = new Scanner(System.in);
        int a = sc.nextInt();

        if(a%2==0){
            System.out.println(a+" is is even");

        }
        else{
            System.out.println(a+" is Odd");
        }

    }
}
